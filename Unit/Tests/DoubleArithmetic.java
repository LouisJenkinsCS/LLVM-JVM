class DoubleArithmetic {
	public static void main(String[] args) {
		double x = 1.2345;
		double y = 6.78910;
		double z = 11.12131415;

		System.out.println("x: " + x);
		System.out.println("y: " + y);
		System.out.println("z: " + z);
		System.out.println("x + y + z = " + (x + y + z));
		System.out.println("x - y - z = " + (x - y - z));
		System.out.println("x * y * z = " + (x * y * z));
		System.out.println("x / y / z = " + (x / y / z));
	}
}
